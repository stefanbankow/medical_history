package com.medical.history.dto;

import jakarta.validation.constraints.Email;
import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.Size;
import lombok.Data;

import java.util.Set;

@Data
public class SignupRequest {
    
    @NotBlank(message = "Username is required")
    @Size(min = 3, max = 20, message = "Username must be between 3 and 20 characters")
    private String username;
    
    @NotBlank(message = "Email is required")
    @Email(message = "Email should be valid")
    @Size(max = 50, message = "Email must not exceed 50 characters")
    private String email;
    
    @NotBlank(message = "Password is required")
    @Size(min = 6, max = 40, message = "Password must be between 6 and 40 characters")
    private String password;
    
    private Set<String> role;
    
    // Additional fields for patient/doctor registration
    private String name;
    private String egn; // For patients
    private String identificationNumber; // For doctors
    private String specialty; // For doctors
    private Boolean isFamilyDoctor = false; // For doctors
    private Long familyDoctorId; // For patients
}
